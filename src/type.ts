export class Position {
  private m_x: number | string;
  private m_y: number | string;
  private m_z: number | string;

  constructor(x?: number | string, y?: number | string, z?: number | string) {
    this.m_x = x || 0;
    this.m_y = y || 0;
    this.m_z = z || 0;
  }

  set X(x: number | string) {
    this.m_x = x
  }
  get X() {
    return this.m_x
  }

  set Y(y: number | string) {
    this.m_y = y
  }
  get Y() {
    return this.m_y
  }

  set Z(z: number | string) {
    this.m_z = z
  }
  get Z() {
    return this.m_z
  }

  toString(): string {
    return `${this.m_x} ${this.m_y} ${this.m_z}`;
  }

  add(position: Position) {
    let x1 = 0;
    let y1 = 0;
    let z1 = 0;
    let x2 = 0;
    let y2 = 0;
    let z2 = 0;
    if (typeof this.m_x === 'string' && this.m_x.startsWith('~')) {
      x1 = Number.parseFloat(this.m_x.substring(1)) + <number>global.player.Position.X;
    } else if (typeof this.m_x === 'number') {
      x1 = this.m_x;
    }
    if (typeof this.m_y === 'string' && this.m_y.startsWith('~')) {
      y1 = Number.parseFloat(this.m_y.substring(1)) + <number>global.player.Position.Y;
    } else if (typeof this.m_y === 'number') {
      y1 = this.m_y;
    }
    if (typeof this.m_z === 'string' && this.m_z.startsWith('~')) {
      z1 = Number.parseFloat(this.m_z.substring(1)) + <number>global.player.Position.Z;
    } else if (typeof this.m_z === 'number') {
      z1 = this.m_z;
    }
    if (typeof position.X === 'string' && position.X.startsWith('~')) {
      x2 = Number.parseFloat(position.X.substring(1)) + <number>global.player.Position.X;
    } else if (typeof position.X === 'number') {
      x2 = position.X;
    }
    if (typeof position.Y === 'string' && position.Y.startsWith('~')) {
      y2 = Number.parseFloat(position.Y.substring(1)) + <number>global.player.Position.Y;
    } else if (typeof position.Y === 'number') {
      y2 = position.Y;
    }
    if (typeof position.Z === 'string' && position.Z.startsWith('~')) {
      z2 = Number.parseFloat(position.Z.substring(1)) + <number>global.player.Position.Z;
    } else if (typeof position.Z === 'number') {
      z2 = position.Z;
    }

    return new Position(
      x1 + x2,
      y1 + y2,
      z1 + z2
    )
  }
}

